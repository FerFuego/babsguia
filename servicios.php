	            	<h4>SERVICIO PARA ESCUELAS DE ESPAÑOL</h4><hr>
	            	<p>¿Qué ofrecemos? <br><br>
					Ofrecemos un servicio especialmente diseñado para las Escuelas de Español, que consiste en hacer que una visita guiada por Buenos Aires sea parte de la jornada de estudio de los alumnos.<br><br>
					La experiencia hace la diferencia.<br>
					Sobre la base de nuestra vasta experiencia, cuando organizamos una visita para extranjeros estudiantes de español, tenemos en cuenta los siguientes puntos:<br>
					• El vocabulario utilizado por el Guía Profesional debe ser acorde al nivel de los alumnos.<br>
					• El Guía Profesional debe estar altamente capacitado para transmitir el contenido histórico-cultural e informativo a alumnos de todos los niveles; de esto depende el éxito del servicio.<br>
					• Utilizamos el sistema de pregunta y respuesta para promover el uso del idioma español por parte de cada uno de los integrantes del grupo.<br>
					• Aconsejamos que los grupos sean reducidos para estimular la mayor participación de los alumnos en la actividad, así como también un mejor contacto entre ellos. Si el grupo no es reducido, el Guía Profesional utiliza diferentes técnicas para lograr su integración, teniendo en cuenta el perfil del grupo y de las personalidades que lo conforman.<br>
					• Aconsejamos, también, que el grupo esté constituido por alumnos del mismo nivel para evitar el aburrimiento y la dispersión de algunos de sus integrantes. Si el grupo es heterogéneo en cuanto al dominio del idioma, el guía cuenta con la capacitación necesaria para lograr su integración, considerando las necesidades e intereses de cada alumno. La participación activa de los alumnos de niveles avanzados y la explicación informativa básica personalizada a los de niveles inferiores garantizan el éxito del recorrido.<br>
					• La duración de la visita depende del destino, del interés de los alumnos y del nivel del grupo. Este punto se acuerda con anterioridad. -Para organizar la salida, se contemplan tanto la cantidad de horas como los horarios de estudio de los alumnos en la institución.<br>
					• Es recomendable la utilización de medios de transporte público para las visitas. De esta manera, los alumnos tienen un contacto activo con la ciudad y su gente. No obstante, no descartamos el servicio de transporte privado, si el grupo lo prefiere.<br>
					• Las visitas guiadas se realizan un 50% caminando y un 50% en transporte. La idea de este sistema combinado es que los alumnos accedan a lugares específicos durante el trayecto y logren un mayor contacto con la arquitectura y la historia de la ciudad.<br>
					• Existe un contenido básico en la visita guiada para todos los niveles. En el caso de los más avanzados, y si los alumnos lo desean, se incorpora más información histórica y cultural.<br>
					• Si la institución o los alumnos desean realizar un recorrido temático diferente de los que ofrecemos habitualmente -por ejemplo, basado en el contenido de una clase o de un programa específico-, estamos en condiciones de organizarlo.<br><br>
					El resultado de nuestro servicio<br>
					Sabemos que la visita guiada no sólo les permite a los alumnos conocer gran parte de la ciudad, sino que también resulta para ellos una muy buena oportunidad de utilizar el idioma español fuera del aula, de manera relajada, lo cual favorece la adquisición de confianza y seguridad en sus comunicaciones cotidianas. Es allí donde pueden darse cuenta de cuánto saben o cuánto les falta aprender. Otro beneficio que aporta la visita guiada, de gran valor sobre todo para los más jóvenes, es la posibilidad de relacionarse con personas de diferentes edades y provenientes de otros países, en un clima absolutamente diferente al académico.<br>
